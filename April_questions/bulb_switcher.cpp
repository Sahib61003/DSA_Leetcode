//Problem: Bulb Switcher
//Link: https://leetcode.com/problems/bulb-switcher/

//Code:
class Solution {
public:
    int bulbSwitch(int n) {
        return sqrt(n);
    }
};

/*Explanation of the Code:
  1. Create a function and take integer n as input.
  2. return square root of the integer using function
 */

//Time Complexity: O(log(n))
